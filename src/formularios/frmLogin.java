    public void validar() {

        int resultado = 0;

        try {
            String usuario = txtUsuario.getText();
            String pass = String.valueOf(txtPassword.getPassword());

            String sql = "SELECT * FROM empleados WHERE usuario ='" + usuario + "' and contrasena='" + pass + "' ";

            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);

            if (rs.next()) {

                resultado = 1;

                if (resultado == 1) {

                    JOptionPane.showMessageDialog(null, "Bienvenido al sistema ");
                    frmMenu form = new frmMenu();
                    form.setVisible(true);
                    this.dispose();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Los datos ingresados son incorrectos, intente nuevamente");
                txtUsuario.setText(null);
                txtPassword.setText(null);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Los datos ingresados son incorrectos, intente nuevamente" + e.getMessage());
        }

    }

